class Punto{

    private int x;
    private int y;

    public Punto(int _x, int _y){
        this.x = _x;
        this.y = _y;
    }

    public void setX(int _x){
        this.x = _x;
    }

    public void setY(int _y){
        this.y = _y;
    }

    public void getX(){
        return this.x;
    }

    public void getY(){
        return this.y;
    }
    

}